//VAR e LET

// Declarando VAR
var texto = 'Hello World'
console.log(texto)

// Declarando LET
let nome = 'Luiz Carlos'
console.log('nome:',nome)

// Atribuindo um novo valor
nome = 'Guilherme'
console.log('nome:', nome)

// Atribuindo um novo valor - VAR
texto = 'Olá Mundo'
console.log(texto)

//constantes - const
const valorPi = 3.14
console.log('O valor pi é igual a:',valorPi)

